export async function getWordsByCategory(slug?: string): Promise<Array<Word>> {
    return new Promise((resolve) => {
        if (slug) {
            const word = data.filter(w => w.category === slug);
            resolve(word);
        } else {
            resolve(data);
        }
    })
}

export async function getWordByUid(uid?: string): Promise<Array<Word>> {
    return new Promise((resolve) => {
        if (uid) {
            const word = data.filter(w => w.uid === uid);
            resolve(word);
        } else {
            resolve(data);
        }
    })
}